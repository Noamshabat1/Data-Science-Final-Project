# â”€â”€ 2-B. Mini utility to show a *tidy* snapshot of any DataFrame â”€â”€â”€â”€â”€â”€â”€â”€â”€
def snapshot_df(df: pd.DataFrame,
                label: str,
                *,
                head_n: int = 4,
                preview_n: int = 6,
                baseline: pd.DataFrame | None = None
                ) -> None:
    """
    Compact one-pager for a DataFrame.

    â€¢ headline stats (shape, memory, dtype counts, % rows with â‰¥1 NA)
    â€¢ if *baseline* is supplied, only columns that changed (added OR dtype-fixed)
      are reported and flagged with ğŸ”„
    â€¢ shows up to *preview_n* example columns from head()
    """
    # â”€â”€ headline ----------------------------------------------------------
    mem = df.memory_usage(deep=True).sum() / 1_048_576  # â†’ MiB
    na_pct = df.isna().any(axis=1).mean() * 100
    num, obj, dt = (df.select_dtypes(t).shape[1]
                    for t in ["number", "object", "datetime"])

    print(f"   ğŸ“„ {label:<6} | {df.shape[0]:,}Ã—{df.shape[1]} "
          f"| mem:{mem:6.1f} MiB | num:{num} obj:{obj} dt:{dt} "
          f"| rows w/ NAâ‰¥1:{na_pct:6.2f}%")

    # â”€â”€ diff vs. baseline -------------------------------------------------
    if baseline is not None:
        changed_cols = []
        renamed_cnt = 0
        dtype_cnt = 0

        for c in df.columns:
            if c not in baseline.columns:
                changed_cols.append(c)
                renamed_cnt += 1  # entirely new column
            else:
                if baseline[c].dtype != df[c].dtype:
                    changed_cols.append(c)
                    dtype_cnt += 1  # dtype fixed/changed

        if changed_cols:
            print(f"      â†ª ğŸ”„ {len(changed_cols)} columns adjusted "
                  f"(renamed:{renamed_cnt}, dtype-fixed:{dtype_cnt})")
    else:
        changed_cols = list(df.columns)

    # choose columns to preview
    cols = changed_cols[:preview_n]
    if len(changed_cols) > preview_n:
        cols.append("â€¦")  # visual ellipsis

    safe_cols = [c for c in cols if c in df.columns]  # drop the ellipsis

    # â”€â”€ pretty head() -----------------------------------------------------
    with pd.option_context("display.max_colwidth", 60,
                           "display.width", 120):
        print(df[safe_cols].head(head_n).to_string(index=False))
    print("-" * 60)

